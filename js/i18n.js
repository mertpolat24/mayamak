window.MayamakI18n = (function () {
  var STORAGE_KEY = "mayamak_lang";
  var currentLang = localStorage.getItem(STORAGE_KEY) || "tr";

  function t(key) {
    var dict = window.MAYAMAK_I18N[currentLang] || window.MAYAMAK_I18N.tr;
    return dict[key] || window.MAYAMAK_I18N.tr[key] || key;
  }

  function getLang() {
    return currentLang;
  }

  function setLang(lang) {
    if (!window.MAYAMAK_I18N[lang]) return;
    currentLang = lang;
    localStorage.setItem(STORAGE_KEY, lang);
    document.documentElement.lang = lang;
    applyTranslations();
    document.dispatchEvent(new CustomEvent("mayamak:langchange", { detail: { lang: lang } }));
  }

  function applyTranslations() {
    document.querySelectorAll("[data-i18n]").forEach(function (el) {
      var key = el.getAttribute("data-i18n");
      var val = t(key);
      if (el.tagName === "INPUT" || el.tagName === "TEXTAREA") {
        el.placeholder = val;
      } else {
        el.textContent = val;
      }
    });
    document.querySelectorAll("[data-i18n-html]").forEach(function (el) {
      el.innerHTML = t(el.getAttribute("data-i18n-html"));
    });
    document.querySelectorAll("[data-i18n-title]").forEach(function (el) {
      el.setAttribute("title", t(el.getAttribute("data-i18n-title")));
    });
    var titleKey = document.body.getAttribute("data-page-title");
    if (titleKey) {
      var title = t(titleKey);
      document.title = title;
      var ogTitle = document.querySelector('meta[property="og:title"]');
      if (ogTitle) ogTitle.setAttribute("content", title);
      var twTitle = document.querySelector('meta[name="twitter:title"]');
      if (twTitle) twTitle.setAttribute("content", title);
    }
    var descKey = document.body.getAttribute("data-page-description");
    if (descKey) {
      var desc = t(descKey);
      var metaDesc = document.querySelector('meta[name="description"]');
      if (metaDesc) metaDesc.setAttribute("content", desc);
      var ogDesc = document.querySelector('meta[property="og:description"]');
      if (ogDesc) ogDesc.setAttribute("content", desc);
      var twDesc = document.querySelector('meta[name="twitter:description"]');
      if (twDesc) twDesc.setAttribute("content", desc);
    }
    var ogLocale = document.querySelector('meta[property="og:locale"]');
    if (ogLocale) ogLocale.setAttribute("content", currentLang === "tr" ? "tr_TR" : "en_US");
    document.querySelectorAll(".lang-btn").forEach(function (btn) {
      btn.classList.toggle("active", btn.getAttribute("data-lang") === currentLang);
    });
  }

  function init() {
    document.documentElement.lang = currentLang;
    applyTranslations();
    document.querySelectorAll(".lang-btn").forEach(function (btn) {
      btn.addEventListener("click", function () {
        setLang(btn.getAttribute("data-lang"));
      });
    });
  }

  return { t: t, getLang: getLang, setLang: setLang, init: init, apply: applyTranslations };
})();
